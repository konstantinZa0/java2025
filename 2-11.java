public class DataTypes {
    public static void main(String[] args) {
        // Целочисленные типы
        byte byteVar = 0;           // 0
        short shortVar = 0;         // 0
        int intVar = 0;             // 0
        long longVar = 0L;          // 0
        
        // С плавающей точкой
        float floatVar = 0.0f;      // 0.0
        double doubleVar = 0.0;     // 0.0
        
        // Символьный
        char charVar = '\u0000';    // нулевой символ
        
        // Булевский
        boolean boolVar = false;    // false
    }
}